package sec01;

public class AssignmentOp {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		a += 4; // a = a + 4
		b -= 4; // b = b -4
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		
		a *= 2;
		System.out.println("a = " + a);
		b /= 2;
		System.out.println("b = " + b);

	}

}
