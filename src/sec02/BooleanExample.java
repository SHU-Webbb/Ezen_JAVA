package sec02;

public class BooleanExample {

	public static void main(String[] args) {
		boolean play = false; // boolean type의 값은 true, false
		System.out.println("play = " + play);

		if (play) {
			System.out.println("play중 입니다.");
		} else {
			System.out.println("play중이 아닙니다.");
		}
	}

}




